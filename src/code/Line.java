package code;

public class Line {
	private int number;

	/**
	 * 
	 * @param number that represents the line number
	 */
	public Line(int number) {
		super();
		this.number = number;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}
	
	
}
